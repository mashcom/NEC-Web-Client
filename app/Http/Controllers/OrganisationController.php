    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|unique:organisations|max:50',
            'email' => 'required|unique:organisations|max:50',
        ]);

        $organisation = new Organisation();
        $organisation->name = trim($request->name);
        $organisation->email = trim($request->email);
        $organisation->status='active';
        if($organisation->save()){
            return back()->with('success','Client added successfully');
        }
        return back()->withErrors(['Client could not be added, please try again']);
    }

